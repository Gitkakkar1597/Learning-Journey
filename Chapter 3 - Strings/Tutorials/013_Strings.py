# Strings in python

# Data type in python
# Sequence of characters enclosed within double/single quotes ("") or ('')

# Example usage
name - "Siddharth"      # Single-quote string

name2 = 'Sid'           # Double-quote string

name3 = '''Siddharth Kakkar'''        # Triple-quote string (for multi-line strings)

# NOTE: A string is immutable once defined
# Meaning, characters in a string can't be changed