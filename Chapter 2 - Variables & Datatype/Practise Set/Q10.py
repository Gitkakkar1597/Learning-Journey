# Python program to find an average of two numbers entered by the user

a = int(input("Enter 1st number: "))
b = int(input("Enter 2nd number: "))

print("1st number: ", a)               # 18
print("2nd number: ", b)               # 52

print("Average : ", (a+b)/2)           # 35.0

