# Python program to find remainder when a number is divided by z

n = int(input("Enter a number to be divided: "))
z = int(input("Enter divisor(z): "))

print("Dividend: ", n)                  # 102
print("Divisor: ", z)                   # 7

# n/z

print("Remainder: ", n%z)               # 4

# '%' is 'Modulo' operator which returns remainder of a division operation