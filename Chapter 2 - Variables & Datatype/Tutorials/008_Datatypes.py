# Data types in python

# Types of datatypes in python (primarily):-
'''
1. Integers : Numbers such as 1, 2, 3 or 4
2. Floating point numbers : Decimal numbers such as 7.14, 8.32
3. Strings : Characters inside double-quotes like "siddharth"
4. Booleans : True or False, to identify whether something is yes/no
5. None : To mark whenever a variable has nothing(no value) assigned to it
'''

# Example usage
a = 1   # a is an integer variable

b = 5.22   # b is a floating point number

c = "Siddharth"     # c is a string

d = True            # d is a boolean variable

e = None           # e is a none-type variable
